"""
Basic async main loop
"""

import asyncio
from typing import Awaitable


def main(run: Awaitable):
    """Run main loop"""
    try:
        loop = asyncio.get_event_loop()
    except RuntimeError:
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)

    try:
        loop.run_until_complete(run)
    finally:
        loop.stop()
        loop.close()
